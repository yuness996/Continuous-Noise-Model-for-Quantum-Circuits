# Analytical final sigmas
# ---------------------------------------------------------

def calculate_sigmas_analytical(arr, k):
    """
    Calculate final local Gaussian widths analytically.

    Output
    ------
    q_params[i] = (sigma_theta_i, sigma_phi_i)

    Model
    -----
    - Fresh rotational errors are inserted after each single-qubit gate.
    - Each fresh error adds variance k^2 to theta and phi.
    - H swaps theta-like and phi-like errors.
    - X, Y, Z, S add fresh local noise but do not change local widths
      in this reduced approximation.
    - CX does not add fresh noise.
    - SWAP exchanges the local widths between the two qubits.

    This is a local independent approximation. It does not keep
    two-qubit error strings generated by CX propagation.
    """

    n_qubits = arr[0]
    depth = len(arr) - 1

    # Store variances first, not sigmas.
    var_params = [(sp.Integer(0), sp.Integer(0)) for _ in range(n_qubits)]

    def add_gate_noise(pair):
        vt, vp = pair
        return (vt + k**2, vp + k**2)

    def hadamard_pair(pair):
        vt, vp = pair
        return (vp, vt)

    for i in range(1, depth + 1):
        gate_type, payload = arr[i]

        if gate_type == "h":
            qubit = payload

            # H maps X-like errors to Z-like errors and conversely.
            var_params[qubit] = hadamard_pair(var_params[qubit])

            # Fresh physical noise after H.
            var_params[qubit] = add_gate_noise(var_params[qubit])

        elif gate_type in ("x", "y", "z", "s"):
            qubit = payload

            # In this reduced local-width model these gates do not change
            # the theta/phi widths, but they do add fresh physical noise.
            var_params[qubit] = add_gate_noise(var_params[qubit])

        elif gate_type == "cx":
            # No fresh physical noise after CX in this model.
            # We also do not project two-qubit propagated errors back
            # into local widths.
            continue

        elif gate_type == "swap":
            q0, q1 = payload
            var_params[q0], var_params[q1] = var_params[q1], var_params[q0]
            
        elif gate_type == "barrier":
            # do nothing
            aa = 0

        else:
            raise ValueError(f"Unknown gate type '{gate_type}'")

    q_params = [
        (sp.sqrt(vt), sp.sqrt(vp))
        for vt, vp in var_params
    ]

    return q_params


