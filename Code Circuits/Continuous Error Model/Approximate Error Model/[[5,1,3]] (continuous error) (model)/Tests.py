def qubit_index(q, qc):
    return qc.qubits.index(q)


def circuit_to_array(qc):
    """
    Convert a Qiskit circuit into the list format used by the
    analytical approximation.

    The output has the form:

        [[gate_name, payload], ...]

    CZ is decomposed as:

        CZ(i,j) = H_j CX(i,j) H_j
    """

    flat = transpile(
        qc,
        basis_gates=["h", "x", "y", "z", "cx", "cz", "swap"],
        optimization_level=0,
    )

    ops = []

    for inst in flat.data:
        name = inst.operation.name.lower()
        qubits = [flat.qubits.index(q) for q in inst.qubits]

        if name == "cz":
            i, j = qubits
            ops.append(["h", j])
            ops.append(["cx", [i, j]])
            ops.append(["h", j])

        elif len(qubits) == 1:
            ops.append([name, qubits[0]])

        else:
            ops.append([name, qubits])

    return ops


