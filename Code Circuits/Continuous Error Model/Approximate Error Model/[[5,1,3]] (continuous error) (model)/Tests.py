# Main scan over noise strengths
# ---------------------------------------------------------

def Run1Qbt_AllNoise(a, b, num, shots, times, n_h, correction):
    """
    Run the approximation model for a range of noise strengths.

    The old SVD sampling has been removed. Now we:

        1. Build the encoded circuit.
        2. Convert it to a gate list.
        3. Calculate final qubit-dependent sigmas analytically.
        4. For each sigma, sample one final error per qubit.
        5. Run the approximation circuit.
    """

    n_qubits = 9 if correction else 5

    p_errors = (1 / 3) * np.logspace(a, b, num=num)

    arr = [n_qubits] + circuit_to_array(Circuit(n_h, correction))

    k = sp.symbols("k", positive=True, real=True)

    q_sigmas = calculate_sigmas_analytical(arr, k)

    results = []

    for sig in p_errors:
        sampled_angles = sample_angles_from_sigmas(
            q_sigmas=q_sigmas,
            k_symbol=k,
            sig=sig,
        )

        result = single_encoded_qubit(
            shots=shots,
            times=times,
            correction=correction,
            sampled_angles=sampled_angles,
            k=sig
        )

        results.append(result)

    return results


