def Circuit(n_h, correction):
    """
    Build the encoded circuit used to calculate the analytical final sigmas.

    If correction=False:
        use 5 physical qubits.

    If correction=True:
        use 5 data qubits + 4 ancilla qubits.
    """

    nbq = 4 if correction else 0

    qq = QuantumRegister(5 + nbq)
    cc = ClassicalRegister(5 + nbq)
    qc = QuantumCircuit(qq, cc)

    qc.compose(encode(), range(5), inplace=True)

    for _ in range(n_h):
        qc.append(Logical_Hadamard(), range(5))

    if correction:
        qc.h([2,3])
        #qc.compose(detection(), range(9), inplace=True)
        qc.h(range(5,9))
        qc.h(range(5,9))

    return qc

